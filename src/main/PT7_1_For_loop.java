package main;

public class PT7_1_For_loop {
    public static void main(String[] args) {

        // Loop 01
        int num01 = 0;
        for (int i = 0; i <= 4; i++) {
            System.out.println(i);
        }

        // LOOP 02
        int num02 = 0;
        for (int i = 0; i < 5; ++i) {
            System.out.println(i);
        }

    }
}
